def generate_synthetic_data(profiles, num_questions, samples_per_career):
    data = []

    for career, ranges in profiles.items():
        for _ in range(samples_per_career):
            answers = []
            for i in range(num_questions):
                if i < len(ranges):
                    valid_range = ranges[i]
                else:
                    valid_range = [1, 2, 3, 4]  # If fewer than 18 defined, fill randomly
                answers.append(random.choice(valid_range))
            answers.append(career)
            data.append(answers)

    columns = [f"Q{i+1}" for i in range(num_questions)] + ["Career"]
    return pd.DataFrame(data, columns=columns)

